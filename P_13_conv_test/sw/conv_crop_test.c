static void write_reg(u32 offset, u32 val)
{
    Xil_Out32(WRAPPER_BASE + offset, val);
}

static u32 read_reg(u32 offset)
{
    return Xil_In32(WRAPPER_BASE + offset);
}

static void write_bram_word(u32 bram_addr, u32 val)
{
    Xil_Out32(WRAPPER_BASE + REG_BRAM_BASE + bram_addr, val);
}

static void write_bram_bytes(u32 bram_addr, const s8 *data, int len)
{
    int i = 0;
    for (; i + 3 < len; i += 4) {
        u32 word = ((u32)(u8)data[i])
                 | ((u32)(u8)data[i+1] << 8)
                 | ((u32)(u8)data[i+2] << 16)
                 | ((u32)(u8)data[i+3] << 24);
        write_bram_word(bram_addr + i, word);
    }
    if (i < len) {
        u32 word = 0;
        for (int j = 0; j < len - i; j++) {
            word |= ((u32)(u8)data[i+j]) << (j * 8);
        }
        write_bram_word(bram_addr + i, word);
    }
}

static s8 read_bram_byte(u32 bram_addr)
{
    u32 word_addr = bram_addr & ~0x3u;
    u32 byte_pos  = bram_addr & 0x3u;
    u32 word = Xil_In32(WRAPPER_BASE + REG_BRAM_BASE + word_addr);
    return (s8)((word >> (byte_pos * 8)) & 0xFF);
}

int main(void)
{
    volatile u32 *res = (volatile u32 *)RESULT_ADDR;
    int errors = 0;
    int total_tests = 0;
    u32 ctrl;

    res[0] = 0xAAAA0001;
    Xil_DCacheFlushRange((UINTPTR)res, 64);

    xil_printf("\r\n===================================================\r\n");
    xil_printf("  Conv Crop Test -- YOLOv4 layer-1, 8x8x3->8x8x32\r\n");
    xil_printf("  Weights/bias: REAL (from yolov4_int8_qop.onnx)\r\n");
    xil_printf("  Input: gradient pattern (quantized INT8)\r\n");
    xil_printf("===================================================\r\n\r\n");

    /* ---- Step 1: Write input to BRAM ---- */
    xil_printf("Writing input (%d bytes) to BRAM @ 0x%03X...\r\n",
               INPUT_BYTES, BRAM_INPUT_ADDR);
    write_bram_bytes(BRAM_INPUT_ADDR, input_data, INPUT_BYTES);

    /* ---- Step 2: Write weights (OIHW -> OHWI transpose) ---- */
    xil_printf("Writing weights (%d bytes, OIHW->OHWI) to BRAM @ 0x%03X...\r\n",
               WEIGHT_BYTES, BRAM_WEIGHTS_ADDR);
    {
        s8 w_ohwi[864];
        int kh_sz = 3, kw_sz = 3;
        for (int oc = 0; oc < C_OUT; oc++) {
            const s8 *filt = &weight_data[oc * C_IN * kh_sz * kw_sz];
            s8 *dst = &w_ohwi[oc * C_IN * kh_sz * kw_sz];
            for (int kh = 0; kh < kh_sz; kh++) {
                for (int kw = 0; kw < kw_sz; kw++) {
                    for (int ic = 0; ic < C_IN; ic++) {
                        dst[kh * kw_sz * C_IN + kw * C_IN + ic] =
                            filt[ic * kh_sz * kw_sz + kh * kw_sz + kw];
                    }
                }
            }
        }
        write_bram_bytes(BRAM_WEIGHTS_ADDR, w_ohwi, WEIGHT_BYTES);
    }

    /* ---- Step 3: Write bias ---- */
    xil_printf("Writing bias (%d x int32) to BRAM @ 0x%03X...\r\n",
               BIAS_WORDS, BRAM_BIAS_ADDR);
    for (int i = 0; i < BIAS_WORDS; i++) {
        write_bram_word(BRAM_BIAS_ADDR + i * 4, (u32)bias_data[i]);
    }

    /* ---- Step 4: Clear output area ---- */
    for (int i = 0; i < OUTPUT_BYTES; i += 4) {
        write_bram_word(BRAM_OUTPUT_ADDR + i, 0xDEDEDEDE);
    }

    /* ---- Step 5: Configure conv_engine registers ---- */
    xil_printf("Configuring conv_engine registers...\r\n");
    write_reg(REG_CTRL, 0);
    write_reg(REG_C_IN,  C_IN);
    write_reg(REG_C_OUT, C_OUT);
    write_reg(REG_H_IN,  H_IN);
    write_reg(REG_W_IN,  W_IN);
    write_reg(REG_KSP, (PAD << 3) | (STRIDE << 2) | KSIZE);
    write_reg(REG_X_ZP, (u32)(s32)(-128) & 0x1FF);
    write_reg(REG_W_ZP, 0);
    write_reg(REG_M0, 656954014u);
    write_reg(REG_N_SHIFT, 37);
    write_reg(REG_Y_ZP, (u32)(s32)(-17) & 0xFF);
    write_reg(REG_ADDR_INPUT,   BRAM_INPUT_ADDR);
    write_reg(REG_ADDR_WEIGHTS, BRAM_WEIGHTS_ADDR);
    write_reg(REG_ADDR_BIAS,    BRAM_BIAS_ADDR);
    write_reg(REG_ADDR_OUTPUT,  BRAM_OUTPUT_ADDR);
    write_reg(REG_IC_TILE_SIZE, C_IN);

    /* ---- Step 6: Start conv_engine ---- */
    xil_printf("Starting conv_engine...\r\n");
    write_reg(REG_CTRL, 1);

    /* ---- Step 7: Poll for done ---- */
    int timeout = 0;
    do {
        ctrl = read_reg(REG_CTRL);
        timeout++;
        if (timeout > 10000000) {
            xil_printf("ERROR: Timeout! ctrl=0x%08X\r\n", ctrl);
            res[0] = MAGIC_DONE;
            res[1] = 0;
            res[2] = 99;
            Xil_DCacheFlushRange((UINTPTR)res, 64);
            while(1);
        }
    } while ((ctrl & 0x02) == 0);

    xil_printf("Conv_engine DONE (polls=%d, ctrl=0x%08X)\r\n\r\n", timeout, ctrl);
    write_reg(REG_CTRL, 0);

    /* ---- Step 8: Verify pixel(0,0) for all 32 channels ---- */
    xil_printf("=== Pixel (0,0) - all 32 output channels ===\r\n");
    for (int oc = 0; oc < 32; oc++) {
        u32 addr = BRAM_OUTPUT_ADDR + oc * (H_OUT * W_OUT) + 0;
        s8 got = read_bram_byte(addr);
        s8 exp = expected_pixel00[oc];
        int ok = (got == exp);
        if (!ok) errors++;
        total_tests++;
        xil_printf("  oc %2d: got %4d  exp %4d  %s\r\n", oc, (int)got, (int)exp,
                   ok ? "OK" : "FAIL");
    }

    /* ---- Step 9: Verify channel 0, all 64 pixels ---- */
    xil_printf("\r\n=== Channel 0 - all 64 pixels (8x8) ===\r\n");
    for (int oh = 0; oh < H_OUT; oh++) {
        for (int ow = 0; ow < W_OUT; ow++) {
            u32 addr = BRAM_OUTPUT_ADDR + 0 * (H_OUT * W_OUT) + oh * W_OUT + ow;
            s8 got = read_bram_byte(addr);
            s8 exp = expected_ch0_all[oh * W_OUT + ow];
            int ok = (got == exp);
            if (!ok) errors++;
            total_tests++;
            if (!ok || ow == 0 || ow == W_OUT - 1) {
                xil_printf("  (%d,%d): got %4d  exp %4d  %s\r\n", oh, ow,
                           (int)got, (int)exp, ok ? "OK" : "FAIL");
            }
        }
    }

    /* ---- Step 10: Full comparison - all 2048 bytes ---- */
    xil_printf("\r\n=== Full comparison - 2048 bytes ===\r\n");
    int full_errors = 0;
    for (int i = 0; i < OUTPUT_BYTES; i++) {
        u32 addr = BRAM_OUTPUT_ADDR + i;
        s8 got = read_bram_byte(addr);
        s8 exp = expected_full[i];
        if (got != exp) {
            full_errors++;
            if (full_errors <= 20) {
                int oc = i / (H_OUT * W_OUT);
                int rem = i % (H_OUT * W_OUT);
                int oh = rem / W_OUT;
                int ow = rem % W_OUT;
                xil_printf("  MISMATCH [%d] oc=%d (%d,%d): got %4d  exp %4d\r\n",
                           i, oc, oh, ow, (int)got, (int)exp);
            }
        }
    }
    errors += full_errors;
    total_tests += OUTPUT_BYTES;

    if (full_errors > 20) {
        xil_printf("  ... and %d more mismatches\r\n", full_errors - 20);
    }

    xil_printf("\r\nFull: %d/%d match (%d errors)\r\n",
               OUTPUT_BYTES - full_errors, OUTPUT_BYTES, full_errors);

    /* ---- Summary ---- */
    xil_printf("\r\n===================================================\r\n");
    if (full_errors == 0) {
        xil_printf("  PASS: 2048/2048 -- BIT-EXACTO\r\n");
        xil_printf("  YOLOv4 layer-1 crop verified on conv_engine_v2!\r\n");
    } else {
        xil_printf("  FAIL: %d errores de %d\r\n", full_errors, OUTPUT_BYTES);
    }
    xil_printf("===================================================\r\n");

    /* Dump all 2048 raw output bytes to DDR for XSCT inspection */
    volatile u8 *dump = (volatile u8 *)(RESULT_ADDR + 0x100);
    for (int i = 0; i < OUTPUT_BYTES; i++) {
        dump[i] = (u8)read_bram_byte(BRAM_OUTPUT_ADDR + i);
    }
    Xil_DCacheFlushRange((UINTPTR)dump, OUTPUT_BYTES);

    /* Signal to XSCT */
    res[0] = MAGIC_DONE;
    res[1] = (u32)OUTPUT_BYTES;  /* total tests (full comparison) */
    res[2] = (u32)full_errors;
    Xil_DCacheFlushRange((UINTPTR)res, 64);

    while(1);
    return 0;
}
